# python introduction course

# the basic - Print function.

# The print function writes on the console whatever is set as its arguments
# words, phrases requires either "" or '' to work
print("Hello, World!")
print('Today is 09/02/2026')

#the print function will always result in a new line, you can end on the same line using the argument end
print("Hello", end=" ")
print("World")

#the print function also can show numbers, unlike texts, numbers don't require "" or ''
print(1)
print(10)
print(100)

#You can even make calculations inside a print function
print(1 + 2)
print(10 * 5)

#It is possible to combine printing numbers and texts, using comma , .
print("Hello there, I'm ", 28, " years old!")
